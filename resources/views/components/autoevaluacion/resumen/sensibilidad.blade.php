      <h5><strong>0-35: Baja sensibilidad.</strong> </h5>
      <p>Comprendemos que tu nivel de sensibilidad aún está en desarrollo. ¡Es un excelente punto de partida para crecer! Te invitamos a:</p>
      <p class="font-bold my-2">Consejos:</p>
      <ul class="[&_span]:underline [&_span]:font-bold">
        <li><span>Informarte:</span> busca información confiable sobre las diferentes discapacidades, identidades y experiencias. Películas, documentales, libros y testimonios
          personales pueden ser de gran ayuda.</li>
        <li><span>Escuchar:</span> Presta atención a las historias de las personas que viven estas realidades. Sus experiencias te permitirán comprender mejor sus desafíos y
          necesidades.</li>
        <li><span>Cuestionar tus creencias:</span> reflexiona sobre tus propios prejuicios y estereotipos. ¿De dónde vienen? ¿Cómo afectan tu forma de ver el mundo?</li>
        <li><span>Empatizar:</span> Ponte en el lugar de las demás personas. Imagina cómo te sentirías si estuvieras en su situación.</li>

      </ul>
      <p class="mt-4">Recuerda que todos podemos aprender y crecer. ¡Cada pequeño paso cuenta!</p>
