      <h5><strong>30-60 Consciente de la inconsciencia.</strong> </h5>
      <p>Eres consciente de que tienes sesgos, pero aún te cuesta manejarlos. </p>
      <p class="font-bold my-2">Consejos:</p>
      <ul class="[&_span]:underline [&_span]:font-bold">
        <li><span>Retrasa el juicio:</span> antes de tomar una decisión importante, tómate un tiempo para reflexionar y considerar diferentes puntos de vista.</li>
        <li><span>Busca evidencias contrarias:</span> activamente busca información que desafíe tus creencias actuales.</li>
        <li><span>Desconfía de las primeras impresiones:</span> No juzgues a las personas o situaciones basándote en una primera impresión.</li>
      </ul>
