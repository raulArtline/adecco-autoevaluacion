      <h5><strong>60-90 Gran nivel de consciencia</strong> </h5>
      <p>Eres muy consciente de tus sesgos y estás trabajando activamente para reducir su impacto.</p>
      <p class="font-bold my-2">Consejos:</p>
      <ul class="[&_span]:underline [&_span]:font-bold">
        <li><span>Practica la empatía:</span> ponte en el lugar de las demás personas y trata de entender sus perspectivas.</li>
        <li><span>Desafía tus propias creencias:</span> pregunta constantemente si tus creencias se basan en evidencia o en sesgos.</li>
        <li><span>Busca retroalimentación:</span> pide a otras personas que te den su opinión honesta sobre tus decisiones y comportamientos.</li>
      </ul>
